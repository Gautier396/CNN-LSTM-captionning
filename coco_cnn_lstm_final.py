"""## Étape 4 — Pré-traitement du texte : `basic_tokenize`

### Pourquoi tokeniser ?
Le LSTM travaille sur des **indices entiers**, pas du texte brut. Avant de construire le vocabulaire ou d'encoder une caption, il faut découper chaque phrase en une liste de **tokens (mots)** propres et standardisés.

### Pipeline appliqué aux captions COCO

Les captions COCO contiennent virgules, points, majuscules, doubles espaces...

```
"A man riding a horse, near the beach!"
  1. lower() + strip()
     → "a man riding a horse, near the beach!"
  2. re.sub(r"[^a-z0-9\s']", " ")   # supprime , . ! ? etc.
     → "a man riding a horse  near the beach "
  3. re.sub(r"\s+", " ")             # normalise les espaces
     → "a man riding a horse near the beach"
  4. split(" ")
     → ["a", "man", "riding", "a", "horse", "near", "the", "beach"]
```

"""

def basic_tokenize(s: str) -> list:
    # Étape 1 : tout en minuscules + suppression des espaces de début/fin
    # 'Dog' et 'dog' doivent être le même token dans le vocabulaire
    s = s.lower().strip()

    # Étape 2 : suppression de la ponctuation et des symboles spéciaux
    # On garde : lettres (a-z), chiffres (0-9), espaces et apostrophes (')
    # Tout le reste (!, ?, ., ,, ;, :...) est remplacé par un espace
    s = re.sub(r"[^a-z0-9\s']", " ", s)

    # Étape 3 : les remplacements précédents créent parfois des espaces multiples
    # On les écrase en un seul espace
    s = re.sub(r"\s+", " ", s)

    # Étape 4 : découpage de la phrase en liste de tokens
    return s.split(" ")


