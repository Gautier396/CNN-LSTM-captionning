ckpt = torch.load(ckpt_path, map_location=device)
print(f"   Epoch sauvegardee : {ckpt.get('epoch', '?')}")

# ── 2) Reconstruction du vocabulaire depuis le checkpoint ─────────────────
# Le vocab est serialise comme une liste (plus leger que la classe entiere)
vocab      = Vocab()
vocab.itos  = ckpt['vocab_itos']
vocab.stoi  = {w: i for i, w in enumerate(vocab.itos)}  # reconstruction du dict inverse

pad_id = ckpt['pad_id']
bos_id = ckpt['bos_id']
eos_id = ckpt['eos_id']
print(f"Vocabulaire restaure : {len(vocab.itos):,} tokens")

# ── 3) Reconstruction du modele et chargement des poids ───────────────────
model = CaptionModel(
    vocab_size=len(vocab.itos), d_embed=512, hidden=512, pad_id=pad_id
).to(device)
model.load_state_dict(ckpt['model_state'])
model.eval()   # IMPORTANT : dropout OFF en inference
print("Modele charge en mode evaluation")

# ── 4) Pipeline de transformation (identique a l'entrainement !) ──────────
# On DOIT utiliser exactement les memes parametres qu'a l'entrainement
tfm = transforms.Compose([
    transforms.Resize((224, 224)),
    transforms.ToTensor(),
    transforms.Normalize(mean=[0.485, 0.456, 0.406], std=[0.229, 0.224, 0.225]),
])

# ── 5) Fonction de generation pour une image individuelle ─────────────────
@torch.no_grad()
def caption_one(img_url, max_len=30):
    # Télécharge l'image cible via son URL
    response = requests.get(img_url, timeout=10)
    img_pil = Image.open(BytesIO(response.content)).convert('RGB')

    # Applique les transformations + ajoute la dimension batch → [1, 3, 224, 224]
    x = tfm(img_pil).unsqueeze(0).to(device)

    # Encodage visuel : image → vecteur de features
    img_vec = model.encoder(x)

    # Generation de la caption par greedy decoding
    gen_ids = model.decoder.generate_greedy(
        img_vec, bos_id=bos_id, eos_id=eos_id, max_len=max_len
    )[0].tolist()

    # Decodage des indices en texte lisible
    pred = vocab.decode(gen_ids)

    return img_pil, pred

# ── 6) Affichage de N images aleatoires avec comparaison GT / PRED ────────
def show_random_tests(n=6):
    picks = random.sample(list(coco.imgs.keys()), k=min(n, len(coco.imgs)))   # n images aleatoires

    for img_id in picks:
        img_info = coco.loadImgs(img_id)[0]
        img_url  = img_info['coco_url']
        
        img_pil, pred = caption_one(img_url, max_len=30)

        # Premiere caption humaine comme reference de comparaison
        ann_ids = coco.getAnnIds(imgIds=img_id)
        anns = coco.loadAnns(ann_ids)
        gt = anns[0]['caption'] if anns else '(pas de ground truth)'

        # Affichage de l'image
        plt.figure(figsize=(7, 5))
        plt.imshow(img_pil)
        plt.axis('off')
        plt.title(f"ID: {img_id}", fontsize=8, color='gray')
        plt.tight_layout()
        plt.show()

        # Comparaison textuelle GT vs PRED
        print(f"  GT   (humain) : {gt}")
        print(f"  PRED (modele) : {pred}")
        print("-" * 65)

# Lancement sur 6 images COCO aleatoires
